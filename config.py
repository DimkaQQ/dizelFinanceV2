# ── Ключевые слова для автоопределения ───────────────────────────────────────
JULIAN_KEYWORDS = [
    "ветеринар", "ветклиника", "зоомагазин", "зоотовары",
    "petshop", "pet shop", "корм для", "груминг", "зоо",
]
INTERNAL_TRANSFER_KEYWORDS = [
    "сперанский", "speransky", "внутрибанковский перевод",
    "перевод между счетами", "погашение од", "пополнение счета",
]

def is_julian(merchant: str) -> bool:
    m = merchant.lower()
    return any(kw in m for kw in JULIAN_KEYWORDS)

def is_internal_transfer(merchant: str, desc: str = "") -> bool:
    text = (merchant + " " + desc).lower()
    return any(kw in text for kw in INTERNAL_TRANSFER_KEYWORDS)
